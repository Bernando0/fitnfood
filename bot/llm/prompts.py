def analyze_user_context(
    name: str,
    slot_ru: str,
    earlier_today: list[str],
    goal: str | None,
    caption: str | None = None,
) -> str:
    lines = [f"Человек: {name}.", f"Это приём пищи: {slot_ru} (по времени сообщения)."]
    if goal:
        goal_ru = {"lose": "снижение веса", "gain": "набор массы", "maintain": "поддержание"}.get(goal, goal)
        lines.append(f"Цель клиента: {goal_ru} — спрашивай с него по этой цели.")
    if earlier_today:
        lines.append("Сегодня этот человек уже ел: " + "; ".join(earlier_today) + ".")
    else:
        lines.append("Сегодня это первый приём пищи у человека.")
    if caption and caption.strip():
        lines.append(
            f"Человек подписал фото: «{caption.strip()}». ОБЯЗАТЕЛЬНО учитывай эту подпись — "
            f"там может быть состав, способ готовки, граммовка или порция. Если подпись уточняет "
            f"или меняет оценку (например, вес порции, 'без масла', 'двойная'), опирайся на неё, "
            f"а не только на картинку."
        )
    lines.append(random.choice(_STYLE_HINTS))
    lines.append("Распознай блюдо на фото ниже честно и верни JSON по инструкции, в своём голосе.")
    return "\n".join(lines)


