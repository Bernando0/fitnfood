def analyze_user_context(name: str, slot_ru: str, earlier_today: list[str], goal: str | None) -> str:
    lines = [f"Человек: {name}.", f"Это приём пищи: {slot_ru} (по времени сообщения)."]
    if goal:
        goal_ru = {"lose": "снижение веса", "gain": "набор массы", "maintain": "поддержание"}.get(goal, goal)
        lines.append(f"Цель клиента: {goal_ru} — спрашивай с него по этой цели.")
    if earlier_today:
        lines.append("Сегодня этот человек уже ел: " + "; ".join(earlier_today) + ".")
    else:
        lines.append("Сегодня это первый приём пищи у человека.")
    lines.append("Распознай блюдо на фото ниже честно и верни JSON по инструкции, в своём голосе.")
    return "\n".join(lines)


